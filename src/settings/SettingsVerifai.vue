<template>
  <div class="form form-vertical form-large">
    <div class="form-field">
      <label>{{ t('settings.engines.chatModel') }}</label>
      <div class="control-group">
        <ModelSelectPlus v-model="chat_model" :models="chat_models" :disabled="chat_models.length == 0" @change="save" />
        <button @click.prevent="onDelete"><BIconTrash /></button>
        <RefreshButton :on-refresh="getModels" ref="refresh" />
      </div>
    </div>
    <div class="form-field">
      <label>{{ t('settings.engines.vision.model') }}</label>
      <ModelSelectPlus v-model="vision_model" :models="vision_models" :height="300" :disabled="vision_models.length == 0" @change="save" />
    </div>
    <OllamaModelPull :pullable-models="getChatModels()" info-url="https://ollama.com/search" info-text="{{ t('settings.engines.verifai.browseModels') }}" @done="onRefresh"/>
    <div class="form-field">
      <label>{{ t('settings.engines.verifai.apiBaseURL') }}</label>
      <input name="baseURL" v-model="baseURL" :placeholder="defaults.engines.verifai.baseURL" @keydown.enter.prevent="save" @change="save"/>
    </div>
    <div class="form-field">
      <label>{{ t('settings.engines.verifai.keepAlive') }}</label>
      <div class="form-subgroup">
        <input type="text" name="keepAlive" v-model="keepAlive" @change="save" />
        <a href="https://github.com/ollama/ollama/blob/main/docs/faq.md#how-do-i-keep-a-model-loaded-in-memory-or-make-it-unload-immediately" target="_blank">{{ t('common.learnMore') }}</a>
      </div>
    </div>
    <div class="form-field horizontal">
      <input type="checkbox" name="disableTools" v-model="disableTools" @change="save" />
      <label>{{ t('settings.engines.disableTools') }}</label>
    </div>
  </div>
</template>

<script setup lang="ts">

import { computed, ref } from 'vue'
import { store } from '../services/store'
import { t } from '../services/i18n'
import { getChatModels } from '../llms/verifai'
import { ChatModel, defaultCapabilities, Ollama } from 'multi-llm-ts'
import VerifaiEngine from '../llms/verifai'
import Dialog from '../composables/dialog'
import LlmFactory from '../llms/llm'
import defaults from '../../defaults/settings.json'
import RefreshButton from '../components/RefreshButton.vue'
import ModelSelectPlus from '../components/ModelSelectPlus.vue'
import OllamaModelPull from '../components/OllamaModelPull.vue'

const baseURL = ref(null)
const keepAlive = ref('')
const disableTools = ref(false)
const chat_model = ref<string>(null)
const vision_model = ref<string>(null)
const chat_models = ref<ChatModel[]>([])
const refresh = ref(null)

const vision_models = computed(() => {
  return [
    { id: '', name: t('settings.engines.vision.noFallback'), ...defaultCapabilities },
    ...chat_models.value.filter(model => model.capabilities?.vision)
  ]
})

const load = () => {
  // Garantir que o objeto verifai existe
  if (!store.config.engines.verifai) {
    store.config.engines.verifai = {
      baseURL: '',
      keepAlive: '',
      models: {
        chat: [],
        image: []
      },
      model: {
        chat: '',
        image: ''
      }
    }
  }
  
  baseURL.value = store.config.engines.verifai?.baseURL || ''
  chat_models.value = store.config.engines.verifai?.models?.chat || []
  chat_model.value = store.config.engines.verifai?.model?.chat || ''
  vision_model.value = store.config.engines.verifai?.model?.vision || ''
  keepAlive.value = store.config.engines.verifai?.keepAlive || ''
  disableTools.value = store.config.engines.verifai?.disableTools || false
}

const onDelete = () => {
  
  Dialog.show({
    target: document.querySelector('.dialog'),
    title: t('settings.engines.verifai.confirmDelete'),
    text: t('common.confirmation.cannotUndo'),
    confirmButtonText: t('common.delete'),
    showCancelButton: true,
  }).then(async (result) => {
    if (result.isConfirmed) {
      const verifai = new VerifaiEngine(store.config.engines.verifai)
      await verifai.deleteModel(chat_model.value)
      onRefresh()
    }
  })

}

const onRefresh = async () => {
  if (refresh.value) {
    await refresh.value.refresh()
  }
}

const getModels = async (): Promise<boolean> => {

  // load
  const llmManager = LlmFactory.manager(store.config)
  let success = await llmManager.loadModels('verifai')
  if (!success) {
    Dialog.alert(t('common.errorModelRefresh'))
    return false
  }

  // reload
  store.saveSettings()
  load()

  // done
  return true

}

const save = () => {
  // Garantir que o objeto verifai existe
  if (!store.config.engines.verifai) {
    store.config.engines.verifai = {
      baseURL: '',
      keepAlive: '',
      models: {
        chat: [],
        image: []
      },
      model: {
        chat: '',
        image: ''
      }
    }
  }
  
  store.config.engines.verifai.baseURL = baseURL.value
  store.config.engines.verifai.model.chat = chat_model.value
  store.config.engines.verifai.model.vision = vision_model.value
  store.config.engines.verifai.keepAlive = keepAlive.value
  store.config.engines.verifai.disableTools = disableTools.value
  
  store.saveSettings()
}

defineExpose({ load })

</script> 